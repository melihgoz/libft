int	ft_atoi(const char *str)
{
	int		nm;
	long	tmp;

	nm = 1;
	tmp = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t' || *str == '\v'
		|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			nm = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		tmp = (tmp * 10) + (*str - '0') * nm;
		str++;
		if (tmp > 2147483647)
			return (0);
	}
	return (tmp);
}
